export const TEAM_ICON_NAMES = Object.keys(TEAM_ICON_MAP);

/**
 * Renders a team icon. Handles Lucide icon names (new) and legacy emoji strings.
 */
export function TeamIcon({
  icon,
  className = "h-4 w-4",
}: {
  icon?: string | null;
  className?: string;
}) {
  if (!icon) {
    const Default = TEAM_ICON_MAP.UsersRound;
    return <Default className={className} />;
  }

  const LucideComponent = TEAM_ICON_MAP[icon];
  if (LucideComponent) {
    return <LucideComponent className={className} />;
  }

  // Legacy emoji fallback
  return <span>{icon}</span>;
}
